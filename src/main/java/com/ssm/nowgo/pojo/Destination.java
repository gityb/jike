package com.ssm.nowgo.pojo;

public class Destination {
    private Integer id;
    private String city;
    private String area;
    private String temperturelcon;
    private String viewimg;
    private String adds;
    private Integer count;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getArea() {
        return area;
    }

    public void setArea(String area) {
        this.area = area;
    }

    public String getTemperturelcon() {
        return temperturelcon;
    }

    public void setTemperturelcon(String temperturelcon) {
        this.temperturelcon = temperturelcon;
    }

    public String getViewimg() {
        return viewimg;
    }

    public void setViewimg(String viewimg) {
        this.viewimg = viewimg;
    }

    public String getAdds() {
        return adds;
    }

    public void setAdds(String adds) {
        this.adds = adds;
    }

    public Integer getCount() {
        return count;
    }

    public void setCount(Integer count) {
        this.count = count;
    }
}
